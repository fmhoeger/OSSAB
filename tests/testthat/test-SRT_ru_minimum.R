library(psychTestR)
library(OSSAB)
library(testthat)

context("SRT")

app <- AppTester$new("apps/SRT_ru")

app$expect_ui_text("Пожалуйста, введите логин Вперёд")
app$set_inputs(p_id = "aбвгд")
app$click_next()

app$expect_ui_text("Инструкция В этом цикле заданий Вы увидите совокупность из нескольких фигур и постараетесь мысленно повернуть изображение. Вам необходимо определить, какой из пяти представленных ниже вариантов ответа совпадает с исходным изображением. Важно, чтобы отдельно каждая из фигур в исходном изображении не меняла своей формы и положения. На выполнение каждого задания отведено 20 секунд. В случае, если Вы не успели ответить за предоставленное количество времени, нажмите на кнопку ниже для продолжения. Успехов! Вперёд")
app$click_next()
app$expect_ui_text("Пример задания Ниже представлено изображение, составленное из нескольких фигур.Выберите один из пяти вариантов ответа, который является повернутой версией данного изображения. Выберите правильный ответ: Вперёд Показать ответ")
app$click_next()
app$expect_ui_text("Самопроверка Для этого задания правильным ответом является вариант A. Нажмите кнопку Вперёд, чтобы приступить к заданиям. Назад Вперёд")
app$click_next()

app$expect_ui_text("Задание 1. Ниже представлено изображение, составленное из нескольких фигур. Выберите один из пяти вариантов ответа, который является повернутой версией данного изображения. Выберите правильный ответ: Вперёд Далее")
app$click(c("button-A", "A"))
app$expect_ui_text("Задание 2. Ниже представлено изображение, составленное из нескольких фигур. Выберите один из пяти вариантов ответа, который является повернутой версией данного изображения. Выберите правильный ответ: Вперёд Далее")
app$click(c("button-B", "B"))
app$click(c("button-A", "A"))
app$click(c("button-A", "A"))
app$click(c("button-A", "A"))
app$click(c("button-A", "A"))
app$click(c("button-A", "A"))
app$click(c("button-A", "A"))
app$click(c("button-A", "A"))
app$click(c("button-A", "A"))
app$click(c("button-A", "A"))
app$click(c("button-B", "B"))
app$click(c("button-B", "B"))
app$click(c("button-A", "A"))
app$expect_ui_text("Задание 15. Ниже представлено изображение, составленное из нескольких фигур. Выберите один из пяти вариантов ответа, который является повернутой версией данного изображения. Выберите правильный ответ: Вперёд Далее")
app$click(c("button-A", "A"))

app$expect_ui_text("Врезультат Вперёд")
app$click_next()

app$expect_ui_text("Ваш результат записан. Это окно можно закрыть.")

results <- app$get_results() %>% as.list()

expect_equal(names(results), c("SRT"))
expect_equal(
  results[["SRT"]],
  list(
    i1 = "A",
    i2 = "B",
    i3 = "A",
    i4 = "A",
    i5 = "A",
    i6 = "A",
    i7 = "A",
    i8 = "A",
    i9 = "A",
    i10 = "A",
    i11 = "A",
    i12 = "B",
    i13 = "B",
    i14 = "A",
    i15 = "A",
    score = 0
  )
)

app$stop()
