app$expect_ui_text("Задание 16. Какая рукоятка будет поворачиваться быстрее всего? Выберите правильный ответ А. Б. В. Далее")
app$click("B")

app$expect_ui_text("Ваш результат записан. Это окно можно закрыть.")

results <- app$get_results() %>% as.list()

