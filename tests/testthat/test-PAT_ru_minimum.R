app$set_inputs(p_id = "aбвгд")
app$click_next()

app$expect_ui_text("Инструкция Вам будут показаны группы фигур. Постарайтесь определить, какая фигура из вариантов ответа может быть получена при соединении фигур из задания. Буквы, обозначающие стороны, покажут Вам, как соединить фигуры. На каждое задание дается 20 секунд, после выбора ответа следующее задание загружается автоматически. Если отсчет времени закончится прежде, чем Вы ответите, просто нажмите на кнопку Далее для перехода к следующему заданию. Удачи! Нажмите на кнопку ниже, чтобы продолжить. Вперёд")
app$click_next()
app$expect_ui_text("Пример задания Постарайтесь определить, какая фигура из вариантов ответа может быть получена при соединении фигур из задания. Если Вы не уверены, нажмите кнопку Вперёд, чтобы увидеть ответ. Выберите правильный ответ: Вперёд Показать ответ")
